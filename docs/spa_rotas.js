/*
  O que é um SPA e o que são rotas
  - As SPA's ( Single Page Application ) são aplicações Web que tem
  como objetivo trazer uma experiência similar a de um APP desktop
  para o usuário ( sem os reloads comuns aos sites ).

  O que é uma rota ?
  - São os caminhos que usamos no browser para acessar páginas
  específicas de um site.

  Como separar seu APP react em Páginas (sem reload) ?
  - A principal biblioteca que nos permite fazer isso se chama
  React Router.

  npm i react-router-dom --save

  Documentação: https://reacttraining.com/react-router
*/